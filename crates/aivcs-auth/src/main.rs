/// Loads the RSA signing key (PEM) at runtime — it is never embedded in the
/// binary. Set `AIVCS_AUTH_SIGNING_KEY` (PEM contents) or
/// `AIVCS_AUTH_SIGNING_KEY_FILE` (path to a PEM, e.g. an ESO-mounted secret).
/// Returns `None` when unconfigured, so the service fails closed instead of
/// signing with a checked-in key.
fn signing_key_pem() -> Option<String> {
    if let Ok(pem) = std::env::var("AIVCS_AUTH_SIGNING_KEY") {
        if !pem.trim().is_empty() {
            return Some(pem);
        }
    }
    if let Ok(path) = std::env::var("AIVCS_AUTH_SIGNING_KEY_FILE") {
        if let Ok(pem) = std::fs::read_to_string(&path) {
            if !pem.trim().is_empty() {
                return Some(pem);
            }
        }
    }
    None
}

